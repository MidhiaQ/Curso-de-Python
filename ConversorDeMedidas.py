m = float(input('Digite a medida em metros: '))
dm = m * 10
cm = m * 100
mm = m * 1000

dam = m / 10
hm = m / 100
km = m / 1000
print('A medida em decímetro é: {} \nA medida em centímetros é: {}cm \nA medida em milímetros é: {}mm'.format(dm, cm, mm))
